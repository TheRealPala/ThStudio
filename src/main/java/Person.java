public class Person {
    public Person(){}
    public Person(String Name, String Surname, String Date_of_birth, String Iban, int Id){
        this.Name = Name;
        this.Surname = Surname;
        this.Date_of_birth = Date_of_birth;
        this.Iban = Iban;
        this.Id = Id;
    }
    private String Name;
    private String Surname;
    private String Date_of_birth;
    private String Iban;
    private int Id;




}
